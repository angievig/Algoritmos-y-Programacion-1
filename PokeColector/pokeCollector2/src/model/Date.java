package model;

public class Date{
	private int day;
	private int month;
	private int year;

	// constructor

	// get set

	public Date(int day, int month, int year){
		this.day= day;
		this.month= month;
		this.year= year;
	}



	public int getDay(){
		return day;
	}
	
	public void setDay( int d ){
		day = d;
	}







	// to String

}